#!/bin/zsh

# python: sys.stdin = open('input.txt','r')
# c++: freopen("input.txt", "r", stdin);

# g++ --version에서 InstalledDir 경로 추출
INSTALLED_DIR=$(g++ --version | grep 'InstalledDir' | awk '{print $2}')
# bin의 상위 디렉토리로 이동
